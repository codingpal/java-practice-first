package operators;

public class UnaryOperators {

	public static void main(String[] args) {
		int x=4;
		
		x+=5;
		System.out.println("x = " +x);
		
		x-=4;
		System.out.println("x = " +x);
		
		x*=4;
		System.out.println("x = " +x);
		
		x/=5;
		System.out.println("x = " +x);
		
		x%=5;
		System.out.println("x = " +x);
		
	}
}
